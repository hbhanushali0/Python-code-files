# wapp to convert fat to cel

fah = float(input("please enter temperature in fah "))

cel = (fah - 32) * 5 / 9

print("fah = ", fah)

print("cel = ", cel)

print("fah = ", round(fah,2))

print("cel = ", round(cel,2))

print("fah =%.2f " %fah)

print("cel =%.2f " %cel)

print("fah =%.2f cel =%.2f "  %(fah,cel))


